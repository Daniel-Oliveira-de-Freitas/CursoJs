package dandoUmaBrincadaComJava;
public class Calcula {

    public int soma(int x, int y){
        return x + y ;
    }
    public int subtrai(int x, int y){
        return x - y ;
    }

    public int multiplica(int x, int y){
        return x * y ;
    }
    public int divide(int x, int y){
        return x / y ;
    }
    
}
